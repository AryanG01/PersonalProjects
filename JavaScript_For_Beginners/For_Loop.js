var array = []
    //initialization; condition; what to do at end
for (var i = 0; i <= 5; i++){
    array.push(i)
}
console.log(array)

// Even Number Loop

var array = []
    //initialization; condition to stop; what to do at end of each turn until condition is met
for (var i = 0; i < 10; i+= 2){
    array.push(i)
}
console.log(array)

// Odd Number Loop

var array = []
    //initialization; condition; what to do at end
for (var i = 1; i < 10; i+= 2){
    array.push(i)
}
console.log(array)

var array = []

for (var i = 10; i > 5; i--){
    array.push(i)
}
console.log(array)